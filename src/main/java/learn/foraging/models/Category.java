package learn.foraging.models;

/**One of four general item groups: edible (good to eat), medicinal (treats illness), inedible (unpleasant to eat,
 may make you sick, but not poisonous), or poisonous (causes illness or death).
 *
 */
public enum Category {
    EDIBLE,
    MEDICINAL,
    INEDIBLE,
    POISONOUS
}
